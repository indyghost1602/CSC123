
public class hybrid {

	public int fixedWings;
	public boolean rotateVertical;
	public boolean rotateHorizontal;
	
	public hybrid(int fixedWings, boolean rotateVertical, boolean rotateHorizontal) {
		super();
		this.fixedWings = fixedWings;
		this.rotateVertical = rotateVertical;
		this.rotateHorizontal = rotateHorizontal;
	}
	
	
	
}
